function getUpcomingSaturday(baseDate = new Date()) {
  const targetDate = new Date(baseDate);
  const saturday = 6;
  const daysUntilSaturday = (saturday - targetDate.getDay() + 7) % 7;

  targetDate.setDate(targetDate.getDate() + daysUntilSaturday);
  return targetDate;
}

function formatShortDate(date) {
  return `${date.getMonth() + 1}/${date.getDate()}`;
}

function renderDrawTitle() {
  const drawDate = getUpcomingSaturday();
  const drawDateText = formatShortDate(drawDate);

  elements.drawTitle.textContent = `${drawDateText} 쥴로또 자동 선택`;
  elements.deadlineDate.textContent = drawDateText;
}

function observeReveal(target) {
  target.classList.add("reveal");

  if (revealObserver) {
    revealObserver.observe(target);
  } else {
    target.classList.add("is-visible");
  }
}

function shuffle(source) {
  const items = [...source];

  for (let index = items.length - 1; index > 0; index -= 1) {
    const swapIndex = Math.floor(Math.random() * (index + 1));
    [items[index], items[swapIndex]] = [items[swapIndex], items[index]];
  }

  return items;
}

function pick(source, count, unique = true) {
  if (!source.length) return [];
  if (unique) return shuffle(source).slice(0, Math.min(count, source.length));

  return Array.from({ length: count }, () => source[Math.floor(Math.random() * source.length)]);
}

function createArmorPick() {
  return {
    hunter: pick(itemDatabase.hunterArmor, 2),
    titan: pick(itemDatabase.titanArmor, 2),
    warlock: pick(itemDatabase.warlockArmor, 2),
  };
}

function createWeaponPick() {
  return {
    hawkmoon: pick(itemDatabase.hawkmoonPerks, 1),
    hawkmoonGrip: pick(itemDatabase.hawkmoonGrips, 1),
    weapons: pick(itemDatabase.exoticWeapons, 3),
    catalysts: pick(itemDatabase.catalysts, 2),
  };
}

function renderEditableList(target, items, databaseKey, stateRoot, stateKey, label) {
  target.replaceChildren(
    ...items.map((item, index) => {
      const listItem = document.createElement("li");
      const picker = document.createElement("div");
      const button = document.createElement("button");
      const menu = document.createElement("div");
      const selectedItems = new Set(items.filter((_, itemIndex) => itemIndex !== index));

      picker.className = "pick-picker";
      button.className = "pick-choice";
      button.type = "button";
      button.textContent = item;
      button.setAttribute("aria-label", `${label} ${index + 1}번 선택`);
      button.setAttribute("aria-expanded", "false");
      menu.className = "pick-menu";

      itemDatabase[databaseKey].forEach((optionName) => {
        const option = document.createElement("button");
        const isSelectedElsewhere = selectedItems.has(optionName);

        option.className = "pick-option";
        option.type = "button";
        option.textContent = optionName;
        option.disabled = isSelectedElsewhere;
        option.classList.toggle("selected", optionName === item);

        option.addEventListener("click", () => {
          resultState[stateRoot][stateKey][index] = optionName;
          renderResults();
          elements.statusText.textContent = "선택 항목이 수정되었습니다. 클립보드 저장 시 수정된 결과가 반영됩니다.";
        });

        menu.append(option);
      });

      button.addEventListener("click", (event) => {
        event.stopPropagation();
        const shouldOpen = !picker.classList.contains("open");

        closePickers();
        picker.classList.toggle("open", shouldOpen);
        button.setAttribute("aria-expanded", String(shouldOpen));
      });

      picker.append(button, menu);
      listItem.append(picker);
      return listItem;
    }),
  );
}

function closePickers() {
  document.querySelectorAll(".pick-picker.open").forEach((picker) => {
    picker.classList.remove("open");
    picker.querySelector(".pick-choice")?.setAttribute("aria-expanded", "false");
  });
}

function renderResults() {
  const armor = resultState.armor;
  const weapon = resultState.weapon;

  if (armor) {
    renderEditableList(elements.hunterResults, armor.hunter, "hunterArmor", "armor", "hunter", "헌터 방어구");
    renderEditableList(elements.titanResults, armor.titan, "titanArmor", "armor", "titan", "타이탄 방어구");
    renderEditableList(elements.warlockResults, armor.warlock, "warlockArmor", "armor", "warlock", "워록 방어구");
  }

  if (weapon) {
    renderEditableList(elements.hawkmoonResults, weapon.hawkmoon, "hawkmoonPerks", "weapon", "hawkmoon", "매의 달 제3퍽");
    renderEditableList(elements.hawkmoonGripResults, weapon.hawkmoonGrip, "hawkmoonGrips", "weapon", "hawkmoonGrip", "매의 달 제4퍽");
    renderEditableList(elements.weaponResults, weapon.weapons, "exoticWeapons", "weapon", "weapons", "경이 무기");
    renderEditableList(elements.catalystResults, weapon.catalysts, "catalysts", "weapon", "catalysts", "촉매제");
  }
}

function setMode(mode) {
  resultState.mode = mode;

  elements.modeTabs.forEach((tab) => {
    tab.classList.toggle("active", tab.dataset.mode === mode);
  });

  elements.armorGroup.classList.toggle("hidden", mode === "weapon");
  elements.weaponGroup.classList.toggle("hidden", mode === "armor");
}

function drawByMode() {
  if (resultState.mode === "all" || resultState.mode === "armor") {
    resultState.armor = createArmorPick();
  }

  if (resultState.mode === "all" || resultState.mode === "weapon") {
    resultState.weapon = createWeaponPick();
  }

  renderResults();
  elements.statusText.textContent =
    "자동 선택이 완료되었습니다. 수정이 필요한 항목은 결과 칸에서 직접 바꿀 수 있습니다.";
}

function ensurePickForCurrentMode() {
  if ((resultState.mode === "all" || resultState.mode === "armor") && !resultState.armor) {
    resultState.armor = createArmorPick();
  }

  if ((resultState.mode === "all" || resultState.mode === "weapon") && !resultState.weapon) {
    resultState.weapon = createWeaponPick();
  }

  renderResults();
}

function formatEntry() {
  const name = elements.playerName.value.trim() || "참여자";
  const lines = [`참여자 : ${name}`, ""];

  if (resultState.armor && resultState.mode !== "weapon") {
    const armorItems = [
      ...resultState.armor.hunter,
      ...resultState.armor.titan,
      ...resultState.armor.warlock,
    ];

    lines.push("방어구 열:");
    lines.push(armorItems.join(" / "));
    lines.push("");
  }

  if (resultState.weapon && resultState.mode !== "armor") {
    const weaponItems = [
      `매의 달 - ${resultState.weapon.hawkmoon[0]} - ${resultState.weapon.hawkmoonGrip[0]}`,
      ...resultState.weapon.weapons,
      ...resultState.weapon.catalysts,
    ];

    lines.push("무기 열:");
    lines.push(weaponItems.join(" / "));
    lines.push("");
  }

  lines.push("[해당 응답은 쥴로또 자동선택 시스템에 의해 생성되었습니다]");

  return lines.join("\n").trim();
}

async function copyEntry() {
  ensurePickForCurrentMode();

  const entry = formatEntry();

  try {
    await navigator.clipboard.writeText(entry);
    elements.statusText.textContent = "응모 형식이 클립보드에 저장되었습니다.";
  } catch (error) {
    elements.statusText.textContent = "클립보드 저장 권한이 없어 아래 형식을 직접 복사해주세요.";
    window.prompt("복사할 응모 형식", entry);
  }
}

